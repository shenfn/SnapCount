const MODE_TO_DOMAIN_KEYS: Record<string, string[]> = {
  finance_cashflow: ["finance"],
  finance_spending_pattern: ["finance"],
  sleep_quality: ["sleep"],
  diet_pattern: ["diet"],
  exercise_pattern: ["exercise"],
  cross_domain_life_review: ["cross_domain", "finance", "sleep", "diet", "exercise"],
};

export function buildDomainHeuristics(route: any): string {
  const modes = [route?.primary_mode, ...(Array.isArray(route?.secondary_modes) ? route.secondary_modes : [])].filter(Boolean);
  const keys = new Set<string>();
  for (const mode of modes) {
    for (const key of MODE_TO_DOMAIN_KEYS[String(mode)] || []) keys.add(key);
  }
  if (keys.size === 0) keys.add("cross_domain");
  return Array.from(keys).map(key => DOMAIN_HEURISTICS[key]).filter(Boolean).join("\n\n");
}
